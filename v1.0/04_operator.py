# 산술 연산자
# +, -, *, /, %, **
print(3 / 5)
print(3 % 5)
print(2 ** 3)
print(divmod(3, 5)) # (몫, 나머지)

# 관계 연산자 (크기 비교 연산자)
print(3 == 3)
print(3 != 3)
print(3 >= 3)
print(3 <= 3)

print(0 < 3 < 5) # 0 < 3 and 3 < 5
print(0 < 3 and 3 < 5)

# 논리 연산자 (and, or, not)
print(3 > 3 and 5 < 7)
print(3 > 3 or 5 < 7)
print(not(3 > 3))
